public class ExemploIfEncadeado {
    public static void main(String[] args) {
        double nota = 8.5;

        if (nota >= 9.0) {
            System.out.println("Conceito A");
        } else if (nota >= 7.0) {
            System.out.println("Conceito B");
        } else if (nota >= 5.0) {
            System.out.println("Conceito C");
        } else {
            System.out.println("Conceito D");
        }
    }
    
}
